public class Country
{
  private String name;
  private int value;

  public Country(String name){
    this.name = name;
    this.value = value;

  }

  public void setValue(int value)
  {
    this.value = value;
  }

  public int getValue()
  {
    return value;
  }

  public String getName()
  {
    return name;
  }


  public void setName(String name)
  {
    this.name = name;
  }

}
